    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        CuentaVO cuentavo = null;
        ClienteVO clientevo = null;
        UsuarioVO usuvo = null;

        String numCuenta = txtNumeroCuenta.getText();
        String titular = txtTitular.getText();
        String saldo = txtSaldo.getText();
        String fechaapertura = txtFecha.getText();
        String estado = txtEstado.getSelectedItem().toString();
        String cedula = txtCedula.getText();
        String telefono = txtTelefono.getText();
        String usuario = txtUsuario.getText();
        String clave = txtClave.getText();
        String rol = txtrol.getSelectedItem().toString();

        String idrol = "";

        if (rol.equals("Cliente")) {

            idrol = "2";

        } else {
            idrol = "1";
        }

        cuentavo = new CuentaVO(numCuenta, numCuenta, titular, saldo, fechaapertura, estado);
        clientevo = new ClienteVO(clave, cedula, titular, telefono, numCuenta, usuario);
        usuvo = new UsuarioVO(usuario, usuario, clave, idrol);

        if (pruebaCuentaDao.crearCuenta(cuentavo, clientevo, usuvo)) {

            mostrarAlerta("Cuenta Registrada");
            menuAdministrador menuA = new menuAdministrador();
            menuA.setVisible(true);
            dispose();
        } else {
            mostrarAlerta("No se pudo registrar la cuenta");
            menuAdministrador menuA = new menuAdministrador();
            menuA.setVisible(true);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

